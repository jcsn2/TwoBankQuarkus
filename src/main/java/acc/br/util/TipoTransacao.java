package acc.br.util;

/**
 * Enum que define os tipos de transações disponíveis no sistema.
 */
public enum TipoTransacao {
    /**
     * Representa uma transação de depósito.
     */
    DEPOSITO,

    /**
     * Representa uma transação de saque.
     */
    SAQUE,

    /**
     * Representa uma transação de transferência.
     */
    TRANSFERENCIA
}
